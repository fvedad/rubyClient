class Book
  def set_title(title)
    @title = title
  end
  def get_title
    @title
  end
  def set_author(author)
    @author = author
  end
  def get_author
    @author
  end
  def set_description(desc)
    @description = desc
  end
  def get_description
    @description
  end
  def set_image_base64(base64String)
    @base64String = base64String
  end
  def get_image_base64
    @base64String
  end
end